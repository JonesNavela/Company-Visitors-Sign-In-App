    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        TrafficRegisterDetails trafficRegisterDetails = new TrafficRegisterDetails();
        
        
        String firstName;
        firstName = txtFirstName.getText();
        trafficRegisterDetails.setFirstName(firstName);
        
        String surname;
        surname = txtSurname.getText();
        trafficRegisterDetails.setSurname(surname);
        
        int age;
        if(!txtAge.getText().isEmpty()) {
           age = Integer.parseInt(txtAge.getText());
           trafficRegisterDetails.setAge(age);
        }
        
        String address;
        address = txtAddress.getText();
        trafficRegisterDetails.setAddress(address);
        
        String traffic;
        traffic = txtTraffic.getText();
        trafficRegisterDetails.setTraffic(traffic);
       
        
        txtOutput.append("First Name:" + " "+ trafficRegisterDetails.getFirstName() + "\n");
        txtOutput.append("Surname:" + " "+ trafficRegisterDetails.getSurname() + "\n");
        txtOutput.append("Age:" + " "+ trafficRegisterDetails.getAge() + "\n");
        txtOutput.append("Address:" + " "+ trafficRegisterDetails.getAddress() + "\n");
        txtOutput.append("Traffic Number:" + " "+ trafficRegisterDetails.getTraffic() + "\n");
        
        boolean successful = save(trafficRegisterDetails);
        
        if(successful){
            txtOutput.append("Succesfuly saved to VLA Database-");
        }
        else {
            txtOutput.append("Details were not saved");
        }
        
        if(rbWesternCape.isSelected())
        {
            txtOutput.append("GHF-481-WC");
        }
        
        else if(rbKwaZuluNatal.isSelected())
        {
            txtOutput.append("CBF-710-KZN");
        }
        
        else if(rbMpumalanga.isSelected())
        {
            txtOutput.append("BLE-463-MP");
        }
        
        else if(rbEasternCape.isSelected())
        {
            txtOutput.append("ANM-205-EC");
        }
        
        else if(rbLimpopo.isSelected())
        {
            txtOutput.append("FRS-481-L");
        }
        
        else if(rbGauteng.isSelected())
        {
            txtOutput.append("PTG-906-GP");
        }
        
        else if(rbNorthernCape.isSelected())
        {
            txtOutput.append("PVD-528-NC");
        }
        
        else if(rbFreeState.isSelected())
        {
            txtOutput.append("VXQ-063-FS");
        }
        
        else if(rbNorthWest.isSelected())
        {
            txtOutput.append("JNC-730-NW");
        }
    }//GEN-LAST:event_btnRegisterActionPerformed

