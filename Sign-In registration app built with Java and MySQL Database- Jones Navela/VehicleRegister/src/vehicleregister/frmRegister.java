    //Database connection to vla DB
    public boolean save(TrafficRegisterDetails trafficRegisterDetails) {
        try {
            Class.forName("com.mysql.cj.jdbc.Driver").newInstance();
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/vla","root","971126");
            //vla is the DB name, root is the username and 971126 is the password
            String query = "Insert INTO trafficregisterdetails(first_name, surname, age, address, traffic) VALUES (?,?,?,?,?)";
            PreparedStatement ps = con.prepareStatement(query);
            ps.setString(1, trafficRegisterDetails.getFirstName());
            ps.setString(2, trafficRegisterDetails.getSurname());
            ps.setInt(3, trafficRegisterDetails.getAge());
            ps.setString(4, trafficRegisterDetails.getAddress());
            ps.setString(5, trafficRegisterDetails.getTraffic());
            ps.execute();
            
            
            con.close();
       
        } catch(Exception e) {
            System.out.println(e);
        return false;
        }
    
        return true;
        
    }
    
    
